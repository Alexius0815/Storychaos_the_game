function normalizeGenreId(lang, genreId) {
  if (genreId && genreId !== "random") return genreId;
  const ids = Object.keys(BACKUP_STORY_BANKS[lang]).filter((entry) => entry !== "random");
  return ids[0];
}

function buildWordPass(words, lang, pass, rng) {
  const fragments = [];
  const rotated = [...words];
  if (pass === 1 && rotated.length > 1) {
    rotated.push(rotated.shift());
  }
  for (let index = 0; index < rotated.length; index += 2) {
    const current = rotated[index];
    const next = rotated[index + 1] || rotated[0];
    const sentence = chooseTemplate(index + Math.floor(rng() * 10), pass, lang)(current, next);
    fragments.push(sentence);
  }
  return fragments.join(" ");
}

export function countBackupVariants(lang, genreId) {
  const actualGenre = normalizeGenreId(lang, genreId);
  const bank = BACKUP_STORY_BANKS[lang]?.[actualGenre];
  if (!bank) return 0;
  return bank.openers.length * bank.settings.length * bank.complications.length * bank.reactions.length * bank.endings.length;
}

export function buildBackupStory({ lang, genreId, words, minChars = 350, salt = "" }) {
  const actualGenre = normalizeGenreId(lang, genreId);
  const bank = BACKUP_STORY_BANKS[lang]?.[actualGenre] || BACKUP_STORY_BANKS[lang]?.alltag;
  const seed = hashString(`${lang}:${actualGenre}:${words.join("|")}:${minChars}:${salt}:${Date.now()}`);
  const rng = createRng(seed);

  const parts = [
    pick(bank.openers, rng),
    pick(bank.settings, rng),
    buildWordPass(words, lang, 0, rng),
    pick(bank.complications, rng),
    buildWordPass(words, lang, 1, rng),
    pick(bank.reactions, rng),
    pick(bank.endings, rng),
  ];

  let story = parts.join(" ").replace(/\s+/g, " ").trim();
  const filler = lang === "de"
    ? [
      "Gerade diese Mischung aus harmloser Fassade und hektischer Selbstkontrolle machte die Geschichte so verdächtig unterhaltsam.",
      "Niemand wollte zu viel verraten, doch genau dieses Bemühen machte alles nur noch sichtbarer.",
      "Am Ende passte vielleicht nicht jedes Detail logisch zusammen, aber das Reaktionschaos wirkte erschreckend rund.",
    ]
    : [
      "That blend of harmless surface and frantic self-control was exactly what made the story so suspiciously entertaining.",
      "Nobody wanted to reveal too much, and that effort somehow made everything easier to read.",
      "By the end, not every detail fit perfectly, but the reaction chaos felt impressively complete.",
    ];

  let fillIndex = 0;
  while (story.length < minChars) {
    story = `${story} ${filler[fillIndex % filler.length]}`.trim();
    fillIndex += 1;
  }
  return story;
}
